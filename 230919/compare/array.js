// array.js
const pokamon = ['피카츄', '라이츄', '이브이', '파이리', '꼬북이']

// push = 배열의 마지막 요소를 추가
pokamon.push('망나뇽')
// pop = 배열의 마지막 요소를 제거 (순서 중요)
pokamon.pop('망나뇽')
// 배열에 이상해씨 추가
pokamon.push('이상해')

console.log(pokamon)
console.log(pokamon[0])
console.log(pokamon[3])

// 배열의 길이를 알아보는 속성 (배열.length)
console.log(pokamon.length)